package mygamevault;

public class Perfil {
	private String nomeusu;
	private String jogofav;
	private String generofav;
	
	public String getNomeusu() {
		return nomeusu;
	}
	public void setNomeusu(String nomeusu) {
		this.nomeusu = nomeusu;
	}
	public String getJogofav() {
		return jogofav;
	}
	public void setJogofav(String jogofav) {
		this.jogofav = jogofav;
	}
	public String getGenerofav() {
		return generofav;
	}
	public void setGenerofav(String generofav) {
		this.generofav = generofav;
	}
	


}